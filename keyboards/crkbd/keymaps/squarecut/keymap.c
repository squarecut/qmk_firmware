uint16_t get_tapping_term(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case RGUI_T(KC_SCLN): case RSFT_T(KC_QUOT):
            return TAPPING_TERM + 200; //- 100;     
        default:
            return TAPPING_TERM + 200;
            
    }
}

